"""

Write a Python program to convert integer to string.

Sample Input:
language = "Python"
version = 3.6
sample Output:
Language: Python, Version: 3.6

"""


def main():
    language = "Python"
    version = 3.6

    print(f"Language: {language}, Version: {version:.2f}")


if __name__ == "__main__":
    main()
