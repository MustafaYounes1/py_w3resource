"""

Write a Python program to find a list with maximum and minimum lengths.

[[0], [1, 3], [5, 7], [9, 11], [13, 15, 17]]
(3, [13, 15, 17])
(1, [0])

[[0], [1, 3], [5, 7], [9, 11], [3, 5, 7]]
(3, [3, 5, 7])
(1, [0])

[[12], [1, 3], [1, 34, 5, 7], [9, 11], [3, 5, 7]]
(4, [1, 34, 5, 7])
(1, [12])

"""


def main():
    data = [
        [[0], [1, 3], [5, 7], [9, 11], [13, 15, 17]],
        [[0], [1, 3], [5, 7], [9, 11], [3, 5, 7]],
        [[12], [1, 3], [1, 34, 5, 7], [9, 11], [3, 5, 7]]
    ]

    for _ in data:
        ma = max(_, key=len)
        mi = min(_, key=len)
        print(len(ma), ma)
        print(len(mi), mi)


if __name__ == "__main__":
    main()
